
public class Ex05실습5 {

	public static void main(String[] args) {

		//
		
		for(int dan = 2; dan<=9;dan++) {
			System.out.print(dan+"단 : ");
			for(int gop = 1; gop<=9;gop++) {
				System.out.print(dan + "*" + gop + "="+ dan*gop + "\t");  // \t를 이용하여 일정한 간격으로 띄어쓰기
			}
			System.out.println();
		}
		
		
		
		
		
		
	}

}
